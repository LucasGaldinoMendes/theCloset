    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int qtdLinhas = gridCompra.getSelectedRowCount();

        if(qtdLinhas < 1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }else if (qtdLinhas == 1){
            int linhaSel = gridCompra.getSelectedRow();

            Compra objDetalhe = new Compra ();
            objDetalhe.setCompraId(Integer.parseInt(gridCompra.getValueAt(linhaSel,0).toString()));
            objDetalhe.setValor(Float.parseFloat(gridCompra.getValueAt(linhaSel,2).toString().replace(",",".")));
            ConsultaCompraDetalhe objCD = new ConsultaCompraDetalhe();
            objCD.compraDetalhe(objDetalhe,this);
            objCD.setVisible(true);
        }else {
            JOptionPane.showMessageDialog(null, "Selecione apenas uma linha para realizar essa operação","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

