    public String verificaFiltro(){
        String filtroSQL = "";
        if (cbFiltro.getSelectedItem().toString() == "Fornecedor") {
            filtroSQL =  "nome";
        }else if (cbFiltro.getSelectedItem().toString() == "Id") {
            filtroSQL = "compras.id";
        }
        else{
            filtroSQL = cbFiltro.getSelectedItem().toString();
        }
        return filtroSQL;   
    }
