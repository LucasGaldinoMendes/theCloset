    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int qtdLinhas = gridVendedor.getSelectedRowCount();
       
        if(qtdLinhas < 1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação");
        }else if (qtdLinhas == 1){
            int linhaSel = gridVendedor.getSelectedRow();
           
            Vendedor objUpdate = new Vendedor ();
            objUpdate.setPessoaId(Integer.parseInt(gridVendedor.getValueAt(linhaSel,0).toString()));
            objUpdate.setNome(gridVendedor.getValueAt(linhaSel,1).toString());
            objUpdate.setDocumento(gridVendedor.getValueAt(linhaSel,2).toString());
            objUpdate.setTelefone(gridVendedor.getValueAt(linhaSel,3).toString());

            CadastroVendedor objCadVend = new CadastroVendedor ();
            objCadVend.update(objUpdate, this);
            objCadVend.setVisible(true);
        }else {
            JOptionPane.showMessageDialog(null, "Selecione apenas uma linha para realizar essa operação","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int qtdLinhas = gridVendedor.getSelectedRowCount();
        
        if(qtdLinhas < 1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação");
        }else if (qtdLinhas == 1){
            int linhaSel = gridVendedor.getSelectedRow();
            Vendedor objDel = new Vendedor ();
            objDel.setPessoaId(Integer.parseInt(gridVendedor.getValueAt(linhaSel,0).toString()));
            objDel.delPessoa(objDel);
            btnPesquisarActionPerformed(evt);
        }else { 
            int [] linhaSel = gridVendedor.getSelectedRows();
            Object[] options = { "Confirmar", "Cancelar" };
            int resposta = JOptionPane.showOptionDialog(null, "Você selecionou mais de um vendedor. Deseja continuar", "Aviso", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);            
            if (resposta == 0){
                Vendedor objDel[] = new Vendedor[qtdLinhas];
                String teste;
                for (int i = 0; i < qtdLinhas; i++) {
                    objDel[i] = new Vendedor();
                    objDel[i].setPessoaId(Integer.parseInt(gridVendedor.getValueAt(linhaSel[i],0).toString()));
                  /*  teste = gridVendedor.getValueAt(linhaSel[i],0).toString();
                    JOptionPane.showMessageDialog(null, "linhaSel- " + linhaSel[i]+ " pessoa id - " + teste);*/
                    objDel[i].delPessoa(objDel[i]);
                }
            } 
            btnPesquisarActionPerformed(evt);
        }  
    }//GEN-LAST:event_btnExcluirActionPerformed

