    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        int qtdLinhas = gridVendedor.getSelectedRowCount();
        if(qtdLinhas < 1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação");
        }else if (qtdLinhas == 1){
            int linhaSel = gridVendedor.getSelectedRow();
            Vendedor obj = new Vendedor ();
            obj.setPessoaId(Integer.parseInt(gridVendedor.getValueAt(linhaSel,0).toString()));
            telaAnterior.addVendedor(obj);
            dispose();
           
        }else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação");
        }
    }//GEN-LAST:event_btnConfirmarActionPerformed

