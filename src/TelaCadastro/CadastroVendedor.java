    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (edita){
            objUpdate.setNome(txtNome.getText());
            objUpdate.setDocumento(txtDocumento.getText());
            objUpdate.setTelefone(txtTelefone.getText());
            objUpdate.setSalario(Float.parseFloat((txtSalario.getText().replace(",", "."))));
            JOptionPane.showMessageDialog(null, "Vendedor alterado com sucesso!" + objUpdate.getNome());
            objUpdate.updateVendedor(objUpdate);
            telaAnterior.atualizar();
            dispose();
        }else{
            Vendedor objVendedor = new Vendedor();
            objVendedor.setNome(txtNome.getText());
            objVendedor.setDocumento(txtDocumento.getText());
            objVendedor.setTelefone(txtTelefone.getText());
            objVendedor.setSalario(Float.parseFloat((txtSalario.getText().replace(",", "."))));
            objVendedor.addVendedor(objVendedor);
            clear();
            txtNome.requestFocus();
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

