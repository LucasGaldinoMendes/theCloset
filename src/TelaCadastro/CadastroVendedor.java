    public void update(Vendedor obj, ConsultaVendedor tela){
        if(obj.getPessoaId() != 0){
            telaAnterior = tela;
            objUpdate.setPessoaId(obj.getPessoaId());
            edita = true;
            txtNome.setText(obj.getNome());
            txtDocumento.setText(obj.getDocumento());
            txtTelefone.setText(obj.getTelefone());
            objUpdate.searchSalario(objUpdate.getPessoaId(),txtSalario);  
        }
        else {
            JOptionPane.showMessageDialog(null, "Problemas ao atualizar o vendedor. Tente novamente!");
        }
    }
    public void clear(){
        txtNome.setText("");
        txtDocumento.setText("");
        txtTelefone.setText("");
        txtSalario.setText("");
    }
