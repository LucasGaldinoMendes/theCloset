    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int qtdItens = Integer.parseInt(txtQtdItens.getText());
        Compra objCompra = new Compra();
        Compra objProdCompra[] = new Compra[qtdItens];
        Produto objProd[] = new Produto[qtdItens];
        
        if (verificarCampos()) {
            objCompra.setValor(Float.parseFloat(txtPrecoTotal.getText().replace(",", ".")));
            objCompra.setFornecedorId(Integer.parseInt(txtFornecedor.getText()));

            for (int i = 0; i < qtdItens; i++) {
                objProdCompra[i] = new Compra();
                objProdCompra[i].setQtd(Integer.parseInt(gridVenda.getValueAt(i,3).toString()));
                objProdCompra[i].setValorProduto(Float.parseFloat(gridVenda.getValueAt(i,4).toString().replace(",", ".")));
                objProdCompra[i].setProdutoId(Integer.parseInt(gridVenda.getValueAt(i,1).toString()));
                
                objProd[i] = new Produto();
                objProd[i].setProdutoId(Integer.parseInt(gridVenda.getValueAt(i,1).toString()));
                objProd[i].setEstoque(Integer.parseInt(gridVenda.getValueAt(i,3).toString()));
            }
            objCompra.setCompraId(objCompra.addCompra(objCompra));
            
            if (objCompra.getCompraId() == 0) {
                 JOptionPane.showMessageDialog(null, "Não foi possível realizar a compra.","Aviso",JOptionPane.INFORMATION_MESSAGE);
            }else{
                for (int i = 0; i < qtdItens; i++) {
                    objCompra.addProdCompra(objProdCompra[i], objCompra.getCompraId());
                }
                for (int i = 0; i < qtdItens; i++) {
                    objProd[i].aumentarEstoque(objProd[i]);
                }
            }
        }
        clear();
        btnVendedor.requestFocus();
    }//GEN-LAST:event_btnSalvarActionPerformed

