    private void btnAddProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProdutoActionPerformed
        int qtd = Integer.parseInt(txtQtd.getText());
        if (objProduto.getEstoque() >= qtd) {
            qtdProdutos++;
            float valorProdutos = Float.parseFloat(txtValor.getText().replace(",", ".")) *  qtd;
            valorTotal += valorProdutos;
            DefaultTableModel gridProdutos = (DefaultTableModel)gridVenda.getModel();
            gridProdutos.addRow(new Object[]{   qtdProdutos,
                objProduto.getProdutoId(),
                objProduto.getDescricao(),
                qtd,
                String.valueOf(objProduto.getValorCompra()).replace(".",",")+"0",
                String.valueOf(valorProdutos).replace(".", ",")+"0"

            }
        );
        updateValorTotal();
        }else {
            JOptionPane.showMessageDialog(null, "Quantidade indisponível no estoque.","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnAddProdutoActionPerformed

