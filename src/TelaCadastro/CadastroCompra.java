    private void btnDelProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelProdutoActionPerformed
        int qtdLinhas = gridVenda.getSelectedRowCount();

        if(qtdLinhas < 1){
            JOptionPane.showMessageDialog(null, "Selecione uma linha para realizar essa operação");
        }else if (qtdLinhas == 1){
            int linhaSel = gridVenda.getSelectedRow();
            qtdProdutos--;
            float valorProdutos = Float.parseFloat(gridVenda.getValueAt(linhaSel,5).toString().replace(",","."));
            valorTotal -= valorProdutos;
            DefaultTableModel gridProdutos = (DefaultTableModel)gridVenda.getModel();
            gridProdutos.removeRow(linhaSel);
            updateValorTotal();

        }else {
            JOptionPane.showMessageDialog(null, "Selecione apenas uma linha para realizar essa operação","Aviso",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDelProdutoActionPerformed

    private void txtProdutoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProdutoKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER){
            Produto objPesquisa = new Produto();
            objProduto = objPesquisa.searchProdutoId(txtProduto);
            if (objProduto.getEstoque() == 0) {
                JOptionPane.showMessageDialog(null, "Código inválido, produto não encontrado." +objProduto.getEstoque() ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
            }else{
                addProd(objProduto);

            }
        }
    }//GEN-LAST:event_txtProdutoKeyPressed

