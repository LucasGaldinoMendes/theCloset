    public void updateValorTotal(){
        txtQtdItens.setText(String.valueOf(qtdProdutos));
        txtPrecoTotal.setText(String.valueOf(valorTotal).replace(".",","));
    }
    public boolean verificarCampos(){
        boolean vend = false, itens = false;
        if (txtFornecedor.getText().isEmpty()) {
            vend = true;
        }
        if (txtQtdItens.getText().isEmpty()){
            itens = true;
        }
        if (txtPrecoTotal.getText().isEmpty()) {
            itens = true;
        }
        
        if (vend || itens) {
            JOptionPane.showMessageDialog(null, "Preencha as informações antes de prosseguir","Aviso",JOptionPane.INFORMATION_MESSAGE);
            return false;
        }else{
            return true;
        }
        
    }
    public void clear(){
        int qtdLinhas = gridVenda.getRowCount();
        DefaultTableModel gridProdutos = (DefaultTableModel)gridVenda.getModel();
        for (int i = 0; i < qtdLinhas; i++) {
            gridProdutos.removeRow(0);
        }
        txtFornecedor.setText("");
        txtProduto.setText("");
        txtQtd.setText("");
        txtDescProduto.setText("");
        txtValor.setText("");
        txtQtdItens.setText("");
        txtPrecoTotal.setText("");
        qtdProdutos = 0;
        valorTotal = 0;
    }
