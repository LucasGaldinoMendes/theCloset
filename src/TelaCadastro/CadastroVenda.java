    public void addProd(Produto obj){
        objProduto = obj;
        txtProduto.setText(String.valueOf(objProduto.getProdutoId()));
        txtDescProduto.setText(objProduto.getDescricao() + " - " +objProduto.getTamanho());
        txtValor.setText(String.valueOf(objProduto.getValorVenda()).replace(".",",")+"0"); 
        txtQtd.setText("1");
        txtQtd.requestFocus();
         
    }
    public void addVendedor(Vendedor obj){
        objVendedor = obj;
        txtVendedor.setText(String.valueOf(objVendedor.getPessoaId()));
    }
    public void updateValorTotal(){
        txtQtdItens.setText(String.valueOf(qtdProdutos));
        txtPrecoTotal.setText(String.valueOf(valorTotal).replace(".",","));
    }
    public boolean verificarCampos(){
        boolean vend = false, itens = false;
        if (txtVendedor.getText().isEmpty()) {
            vend = true;
        }
        if (txtQtdItens.getText().isEmpty()){
            itens = true;
        }
        if (txtPrecoTotal.getText().isEmpty()) {
            itens = true;
        }       
        if (vend || itens) {
             JOptionPane.showMessageDialog(null, "Preencha as informações antes de prosseguir","Aviso",JOptionPane.INFORMATION_MESSAGE);
            return false;
        }else{
            return true;
        }  
    }
    public void clear(){
        int qtdLinhas = gridVenda.getRowCount();
        DefaultTableModel gridProdutos = (DefaultTableModel)gridVenda.getModel();
        for (int i = 0; i < qtdLinhas; i++) {
            gridProdutos.removeRow(0);
        }
        txtVendedor.setText("");
        txtCPF.setText("");
        txtProduto.setText("");
        txtQtd.setText("");
        txtDescProduto.setText("");
        txtValor.setText("");
        txtQtdItens.setText("");
        txtPrecoTotal.setText("");
        qtdProdutos = 0;
        valorTotal = 0;
    }

