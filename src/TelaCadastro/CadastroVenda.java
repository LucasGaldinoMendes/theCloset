    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int qtdItens = Integer.parseInt(txtQtdItens.getText());
        Venda objVenda = new Venda();
        Venda objProdVenda[] = new Venda[qtdItens];
        Produto objProd[] = new Produto[qtdItens];
        if (txtCPF.getText().equals( "   .   .   -  ")) {
            objVenda.setCpf("");
        }else{
            objVenda.setCpf(txtCPF.getText());
            
        }
        if (verificarCampos()) {
            objVenda.setValor(Float.parseFloat(txtPrecoTotal.getText().replace(",", ".")));
            objVenda.setVendedorId(Integer.parseInt(txtVendedor.getText()));
            
            for (int i = 0; i < qtdItens; i++) {
                objProdVenda[i] = new Venda();
                objProdVenda[i].setQtd(Integer.parseInt(gridVenda.getValueAt(i,3).toString()));
                objProdVenda[i].setValorProduto(Float.parseFloat(gridVenda.getValueAt(i,4).toString().replace(",", ".")));
                objProdVenda[i].setProdutoId(Integer.parseInt(gridVenda.getValueAt(i,1).toString()));
                
                objProd[i] = new Produto();
                objProd[i].setProdutoId(Integer.parseInt(gridVenda.getValueAt(i,1).toString()));
                objProd[i].setEstoque(Integer.parseInt(gridVenda.getValueAt(i,3).toString()));
            }
            objVenda.setVendaId(objVenda.addVenda(objVenda));
            
            if (objVenda.getVendaId() != 0) {
                for (int i = 0; i < qtdItens; i++) {
                    objVenda.addProdVenda(objProdVenda[i], objVenda.getVendaId());
                }
                for (int i = 0; i < qtdItens; i++) {
                    objProd[i].baixarEstoque(objProd[i]);
                }
            }else{
                JOptionPane.showMessageDialog(null, "Não foi possível realizar a venda."  ,"Aviso",JOptionPane.INFORMATION_MESSAGE);
            }
            
        }
        clear();
        btnVendedor.requestFocus();
    }//GEN-LAST:event_btnSalvarActionPerformed

